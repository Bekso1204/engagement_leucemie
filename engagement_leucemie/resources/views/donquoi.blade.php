<!-- Paragraphe 4 -->
La compatibilité nécessaire pour une greffe de moelle osseuse fait appel à un système complexe, le HLA, différent de celui des groupes sanguins. La probabilité de trouver deux individus compatibles pour une arête de moelle osseuse en dehors de la fratrie est ainsi d'une sur un million ! Sachant que pour deux membres d'une même fratrie, les probabilités s'élèvent à une sur quatre. C'est pourquoi chaque nouvelle inscription compte et apporte une chance supplémentaire de guérison pour les malades.</p>
