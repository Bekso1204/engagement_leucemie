    <h1>Don de Moelle Osseuse</h1>
<!-- Paragraphe 1 -->
    <p>

        La greffe de moelle osseuse représente un espoir de guérison pour de nombreuses personnes atteintes de cancers ou de maladies graves du système sanguin. La moelle osseuse, source des cellules du sang. La moelle osseuse est située dans les os longs et les os plats - à ne pas confondre avec la moelle épinière, située dans la colonne vertébrale. Elle renferme des cellules très précieuses, appelées cellules souches hématopoïétiques, qui donnent naissance aux cellules du sang : les globules rouges, les globules blancs et les plaquettes.

    </p>
<!-- Titre à quoi sert le don ?-->
    <h3>A quoi sert le don?</h3>
<!-- Paragraphe 2 -->
    <p>

        Pour soigner les maladies du sang, la greffe de moelle osseuse est l'un des traitements efficaces.80 % des greffes de moelle osseuse sont réalisées pour traiter certaines formes de cancer comme les leucémies, les lymphomes, les myélomes... Les personnes atteintes de maladie du sang (thalassémie, drépanocytose), d'anémie aplasique ou de troubles héréditaires du système immunitaire et du métabolisme peuvent également avoir besoin d'une greffe de moelle osseuse.
    </p>
<!-- Titre Qui peut donner -->
    <h3>Qui peut donner?</h3>
<!-- Paragraphe 3 -->
    <p>

        Être volontaire au don de moelle osseuse signifie être inscrit sur le registre France greffe de moelle. <br>
        Pour ce faire, il faut :

    </p>

    <ul>

        <li>

            être en parfaite santé et avoir entre 18 et moins de 36 ans lors de l'inscription, même si on peut donner jusqu'à la veille de ces 61 ans ;
        </li>

        <li>

            passer un entretien médical spécifique pour faire le point sur ses antécédents médicaux et son mode de vie ;

        </li>

        <li>

            s'engager à rester joignable et à se rendre disponible pour les examens médicaux en cas de compatibilité avec un receveur.

        </li>

    </ul>

