<h3>Le prélèvement</h3>

<!-- Paragraphe 6 -->
<P>Il existe aujourd'hui deux façons de prélever les cellules souches hématopoïétiques de la moelle osseuse:<br>
<br>
- Le prélèvement de cellules souches périphériques (environ 80 % des cas) :<br>
Il n'y a pas d'hospitalisation pour ce type de prélèvement. Le donneur reçoit pendant les quelques jours précédant le prélèvement, un médicament favorisant la migration des cellules souches hématopoïétiques de la moelle osseuse vers le sang. Ces cellules sont ensuite recueillies en 3 à 4 heures  via un prélèvement sanguin par aphérèse dans le centre de l'EFS<br>
<br>
- Le prélèvement direct de moelle osseuse (environ 18 % des cas) :<br>
Ce type de prélèvement est réalisé à l'hôpital sous anesthésie générale, par ponction dans les os iliaques (au niveau du bassin). Le donneur sort le lendemain de l'hôpital.<p>
