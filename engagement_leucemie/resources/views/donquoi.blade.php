        La greffe de moelle osseuse représente un espoir de guérison pour de nombreuses personnes atteintes de cancers ou de maladies graves du système sanguin. La moelle osseuse, source des cellules du sang. La moelle osseuse est située dans les os longs et les os plats - à ne pas confondre avec la moelle épinière, située dans la colonne vertébrale. Elle renferme des cellules très précieuses, appelées cellules souches <u>hématopoïétiques</u>, qui donnent naissance aux cellules du sang : les globules rouges, les globules blancs et les plaquettes.

    </p>
